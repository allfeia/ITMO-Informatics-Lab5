<?php
//1 Присвоим значения переменным $a и $b и выведем их значения
$a = 10;
$b = 20;
echo "Значение переменной a: " . $a . "<br>";
echo "Значение переменной b: " . $b . "<br>";
//2 Теперь присвоим переменной $c значение суммы переменных $a и $b, а затем выведем значение $c
$c = $a + $b;
echo "Значение переменной c: " . $c . "<br>";
//3 Увеличим значение переменной $c в три раза и выведем результат
$c *= 3;
echo "Значение переменной c увеличенное в три раза: " . $c . "<br>";
//4 Разделим значение переменной $c на разность переменных $b и $a, выведем результат
$result = $c / ($b - $a);
echo "Результат деления переменной c на разность переменных a и b: " . $result . "<br>";
//5 Зададим новые переменные $p и $q и соединим их значения
$p = "Программа";
$q = "работает";
$result = $p . " " . $q;
echo "Результат объединения переменных p и q: " . $result . "<br>";
//6 Добавим слово "хорошо" к результату при помощи оператора ".="
$result .= " хорошо";
echo "Результат после добавления слова 'хорошо': " . $result . "<br>";
//7 Обменяем значения переменных $q и $w без создания новых переменных
$q = 5;
