}
//9 Напишем цикл, который выводит ненумерованный список из 10 пунктов
echo "<ul>";
for ($i = 1; $i <= 10; $i++) {
  echo "<li>Пункт " . $i . "</li>";
}
echo "</ul>";
//10 Создадим массив из 100 случайных чисел и выведем его при помощи цикла while

$array = array();
for ($i = 0; $i < 100; $i++) {
  $array[] = rand(1, 100);
}

$i = 0;
while ($i < count($array)) {
  echo $array[$i] . " ";
  $i++;
}
//Теперь выведем массив при помощи цикла foreach
foreach ($array as $value) {
  echo $value . " ";
}

//13 Создадим функцию getPlus10(), которая принимает число и распечатывает сумму этого числа и 10
function getPlus10($number) {
  echo "Сумма числа " . $number . " и 10 равна " . ($number + 10) . "<br>";
}

getPlus10(5); // Выведет "Сумма числа 5 и 10 равна 15"
//12* Напишем скрипт, который в зависимости от дня недели, будет выводить надпись при помощи оператора switch
$dayOfWeek = date("l");
switch ($dayOfWeek) {
  case "Monday":
    echo "Сегодня понедельник";
    break;
  case "Tuesday":
    echo "Сегодня вторник";
    break;
  case "Wednesday":
    echo "Сегодня среда";
    break;
  case "Thursday":
    echo "Сегодня четверг";
    break;
  case "Friday":
    echo "Сегодня пятница";
    break;
  case "Saturday":
    echo "Сегодня суббота";
    break;
  case "Sunday":
    echo "Сегодня воскресенье";
    break;
  default:
    echo "Ошибка: неверный день недели";
}
